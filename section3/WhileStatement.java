public class WhileStatement {
    public static void main(String[] args) {
        int i = 0;
        while (i < 5) {
            System.out.println(i);
            i++;
        }

        // do while loop
        int j = 0;
        do {
            System.out.println(j);
            j++;
        }
        while (j < 5);
    }
}
