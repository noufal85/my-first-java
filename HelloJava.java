public class HelloJava {
    public static void main(String[] args) {
        System.out.println("Hello Java");
    }
}

// The syntax "public static void" is a method signature in Java programming language. It specifies the access level, return type, and method name of a Java method.
// "public" is an access modifier that indicates the method can be accessed from anywhere in the program.
// "static" is a keyword that means the method belongs to the class, not to an instance of the class. It can be called without creating an object of the class.
// "void" is the return type of the method, which means the method does not return a value.
// So, a method with the signature "public static void" can be accessed from anywhere, can be called without creating an object of the class, and does not return a value.
// "main": This is the name of the method. It must be spelled exactly as "main" in lowercase letters.
// "(String[] args)": This is the parameter list for the main method. The main method takes a single parameter, which is an array of strings named "args". This parameter is used to pass any command-line arguments that are specified when the program is executed.